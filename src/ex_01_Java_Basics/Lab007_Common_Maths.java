package ex_01_Java_Basics;

public class Lab007_Common_Maths {

    public static void main(String[] args) {

        System.out.println(2+2); // output will be 4
        System.out.println(2*2); // output will be 4
        System.out.println(2-2); // output will be 0
        System.out.println(2/2); // output will be 1
        System.out.println(3/2); // output will be 1
        // if println answer is with decimal (eg. 1.2), it will not take decimal part. it will be shown 1



    }
}
