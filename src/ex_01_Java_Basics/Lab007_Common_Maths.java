package ex_01_Java_Basics;

public class Lab007_Common_Maths {

    public static void main(String[] args) {

        System.out.println("2+2"); // output will be 2+2 - it is a string
        System.out.println(2+2); // output will be 4
        System.out.println(2*2); // output will be 4
        System.out.println(2-2); // output will be 0
        System.out.println(2/2); // output will be 1
        System.out.println(3/2); // output will be 1
        //Rule - if println answer is with decimal (eg. 1.2), it will not take decimal part. it will be shown 1
        //Rule - if we use "" - it is multi corrector string, the output will show as same; for eg. "2+2" - the output will be 2+2
        //Rule - the input in "" will be strings and it will be in green colour in intellij

    }
}
